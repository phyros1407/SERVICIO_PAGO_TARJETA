package beans;

public class TarjetaBean {

	private int id;
	private String tip_tar;
	private String mar_tar;
	private String num_tar;
	private String cvc;
	private int mes_cad;
	private int ano_cad;
	private double saldo;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getTip_tar() {
		return tip_tar;
	}
	public void setTip_tar(String tip_tar) {
		this.tip_tar = tip_tar;
	}
	public String getMar_tar() {
		return mar_tar;
	}
	public void setMar_tar(String mar_tar) {
		this.mar_tar = mar_tar;
	}
	public String getNum_tar() {
		return num_tar;
	}
	public void setNum_tar(String num_tar) {
		this.num_tar = num_tar;
	}
	public String getCvc() {
		return cvc;
	}
	public void setCvc(String cvc) {
		this.cvc = cvc;
	}
	public int getMes_cad() {
		return mes_cad;
	}
	public void setMes_cad(int mes_cad) {
		this.mes_cad = mes_cad;
	}
	public int getAno_cad() {
		return ano_cad;
	}
	public void setAno_cad(int ano_cad) {
		this.ano_cad = ano_cad;
	}
	public double getSaldo() {
		return saldo;
	}
	public void setSaldo(double saldo) {
		this.saldo = saldo;
	}
	
	
	
	
}
